    private void jButtonConfirmerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmerActionPerformed
        // TODO add your handling code here:
        if(jComboBoxClasse.getSelectedIndex()==0){
            jLabel1.setText("Veuillez-selectionner la classe!");
            jLabel1.setVisible(true);}
        else if(jComboBoxJours.getSelectedIndex()==0 || jComboBoxMois.getSelectedIndex()==0){
            jLabel1.setText("Veuillez-specifier la date!");
            jLabel1.setVisible(true);}
        else jLabel1.setVisible(false);
        
        if(jLabel1.isVisible()==false){
            jTable1.setEnabled(true);
            Eleve tabe[]=new Eleve[30];
        Absence ab[]=new Absence[100];
        String o=jComboBoxJours.getSelectedItem().toString()+"+"+jComboBoxMois.getSelectedItem().toString();
        ab=Absence.AfficherAbsences(Classe.ChargerClasseParNom(jComboBoxClasse.getSelectedItem().toString()).getId(),o);
        tabe=Eleve.RechercherEleveParClasse(Classe.ChargerClasseParNom(jComboBoxClasse.getSelectedItem().toString()).getId());
        DefaultTableModel model =(DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        if(tabe!=null){
            for(int i=0;i<tabe.length;i++){
                model.addRow(new Object[]{tabe[i].getId(),tabe[i].getNom(),tabe[i].getPrenom(),false,false,false,false,false});
                if(ab!=null){
                    try{
                     for(int j=0;j<ab.length;j++){
                         if(ab[j].getEleve().getId()==tabe[i].getId()){
                         String a=ab[j].getPeriode();
                         int k=0;
                          while(k<a.length()){
                          int b=Character.getNumericValue(a.charAt(k))+2;
                          model.setValueAt(true,i,b);
                          k++;}
                          break;
                         }
                      }}
                    catch(java.lang.NullPointerException e){}
                }
            }
            
            jTable1.setModel(model);}}

        
        
    }//GEN-LAST:event_jButtonConfirmerActionPerformed


