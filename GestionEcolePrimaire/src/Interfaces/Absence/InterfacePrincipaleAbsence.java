    private void jButtonEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnregistrerActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model =(DefaultTableModel) jTable1.getModel();
        String a=jComboBoxJours.getSelectedItem().toString()+"+"+jComboBoxMois.getSelectedItem().toString();
        for(int i=0;i<model.getRowCount();i++)
             Absence.SupprimerAbsence((int)model.getValueAt(i, 0),a);
        for(int j=0;j<model.getRowCount();j++){
            Absence x=new Absence();
            int m=0;
            x.setEleve(Eleve.ChargerEleve((int)model.getValueAt(j, 0)));
            x.setDate(jComboBoxJours.getSelectedItem().toString()+"+"+jComboBoxMois.getSelectedItem().toString());
            String s="";
            for(int i=3;i<8;i++){
                if((boolean)model.getValueAt(j,i)==true)
                {s+=i-2;
                m++;}
                }
            x.setPeriode(s);
            if(m!=0){
                if(Absence.ChargerAbsence(x.getEleve().getId(),x.getPeriode())!=null)
                    x.ModifierAbsence(x.getEleve().getId(), x.getDate());
                else x.AjouterAbsence();
            }
            
        }
    }//GEN-LAST:event_jButtonEnregistrerActionPerformed

